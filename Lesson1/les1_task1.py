# Выполнить логические побитовые операции «И», «ИЛИ» и др. над числами 5 и 6.
# Выполнить над числом 5 побитовый сдвиг вправо и влево на два знака.
print (f' логичекое ИЛИ 5|6: {5|6}')
print (f' логичекое И 5&6: {5&6}')
print (f' логичекое исключающие ИЛИ 5^6: {5^6}')
print (f' побитовый сдвиг 5 на два байта влево: {5<<2}')
print (f' побитовый сдвиг 5 на два байта вправо: {5>>2}')